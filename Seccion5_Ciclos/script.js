//Los ciclos while y do while son similares, la unica diferencia es que el bucle do while ejecuta al menos una vez la condicion antes de validarla:
let numero = 0
do{
    numero++
}while (numero<=10);
//Otro ciclo bastante conocido es el ciclo for:
for(let constante = 0; constante<10; constante++){
    console.log(constante);
}